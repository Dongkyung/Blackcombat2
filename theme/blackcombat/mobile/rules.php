위의 사항들은 전부 반칙으로 취급하며 주심의 판단에 따라 추가적인 반칙 사항이 있을 수 있다.<br /><br /><br />


<h3>제 8 조 주의/경고/실격</h3>
1. ‘주의/경고/실격’은 주심이 해당 선수에게 ‘주의/경고/실격’라고 육성으로 선언한다.<br />
2. 주심은 주의/경고/실격 선언 시, 선수에게 그 상황을 확인시킨다.<br />
3. 배심의 채점에서 양 선수가 동점일 경우, 주의/경고를 받은 선수에게 불리하게 작용된다.<br />
4. 반칙을 행한 선수는 주의를 받고 감점이 주어진다.<br />
(1) 대기실에서 타 선수에게 피해를 주거나 소란을 피워 피해를 주는 일이 발생될 때 대기실을 운영하고 있는 심판의 판단에 의해 경기 전 주의를 줄 수 있고 대기실운영 심판은 대회사와 다른 심판진에게 알려 이를 첨부토록 한다.<br />
5. 선수의 심각하고 악질적인 반칙행위는 즉각적인 실격의 대상이 된다.<br />
(1) 스포츠쉽에 어긋나며 비신사적이고 악질적인 고의성이 다분한 반칙에 대해서 심판진은 바로 ‘경고’를 선언하여 해당 선수에게 실격패 혹은 반칙패를 선언할 수 있다.<br />
7. 그라운드 상황에서 반칙 및 반칙성 행위를 하였을 경우 경기 진행.<br />
(1) 상위 포지션 공격자가 반칙, 주의 후 스탠딩에서 진행.<br />
(2) 하위 포지션 방어자가 반칙, 주의 후 동일 포지션에서 진행.<br />
8. 반칙을 당한 선수는 주심 또는 대회사의 판단에 따라 최소 30초에서 최대 3분간의 회복 시간을 가질 수 있다.<br />
9. 만약 반칙을 당한 선수가 회복을 하지 못하고 주심 혹은 닥터의 판단으로 경기를 할 수 없으면 반칙을 행한 선수는 반칙패로 처리한다.<br />
10. 선수 불참, 체중 계측 심사 불합격 시에도 실격패로 처리한다.<br /><br /><br />


<h3>제 9 조 승패결정</h3>
1. 공격승 Knock Down/Tap Out/Referee Stop<br />
(1) 선수가 상대 선수 공격에 의해 쓰러져서 더 이상 경기 지속이 무의미하다고 판단될 경우, 주심은 공격 선수에게 Knock Out 승을 선언한다.<br />
(2) 선수가 상대방의 타격 공격, 관절기, 조르기 등에 의하여 포기의사(Tap Out)를 밝힌 경우 주심은 해당 선수에게 승을 선언한다.<br />
(3) 상대의 공격을 방어하는 선수가 계속되는 공격을 회피할 방법이 없어 그 패배가 명확히 예측되는 경우, 주심은 선수 보호 차원에서 경기를 중지하고 해당 공격 선수에게 선수공격승(Referee Stop)을 선언한다.<br />
2. Technical Knock Out (TKO)<br />
(1) 경기 진행 도중에 양 선수 또는 한 선수가 경기를 계속할 수 없을 정도로 심각한 부상이 발생하였을 경우, 주심은 경기를 중지시키고 닥터의 최종 판단으로 경기 속개 여부와 선수의 승패를 결정한다.<br />
(2) 선수가 상대방의 정당한 공격을 받고, 부상을 입은 경우, 닥터가 부상 선수의 경기 진행이 불가능하다고 판단하면 주심은 경기를 중단하고 해당 선수가 패한 것으로 간주한다.<br />
(3) 부상이 상대방의 반칙에 의한 것일 경우, 주심은 반칙을 당한 선수에게 반칙승을 선언한다.<br />
(5) 경기 진행 도중 한쪽 선수의 세컨드가 타월을 케이지에 투입하여 시합을 중지시킨 경우, 주심은 즉각 경기를 중지하고 상대 선수에게 승리를 선언한다.<br />
3. 기권승 Give Up<br />
선수 본인이 경기 전 또는 도중에 경기의 시작, 진행을 포기한 경우, 주심은 상대 선수에게 기권승을 선언한다.<br />
4. 판정승<br />
(1) 판정은 각 라운드 점수를 합산하여 합산 점수가 더 높은 선수가 승리한다.<br />
(2) 더 많은 데미지를 입힌 선수 혹은 더 공격성이 좋은 선수가 승리한다.<br />
(3) 매 경기의 마지막 라운드에 비중을 더 많이 두고 판정을 한다.<br />
(4) 2명 이상의 배심으로부터 승리 판정을 획득한 경우에만 판정승이 성립하고, 그 이외의 경우는 무승부로 간주한다.<br />
(5) 사전에 대회사가 허가하고, 양 선수 합의가 있는 경우, 배심의 판정이 최종적으로 무승부이면 연장 5분 1라운드 후 재판정을 할 수도 있다.<br />
5. 배심 판정의 결정요인<br />
경기에 대한 최종 판정 결과는 3명의 케이지 배심에 의해서 이루어진다.<br />
(1) 타격 배점 기준<br />
1) 단발성의 그 어떤 공격이라도 데미지 정도가 크진 않지만 타격성이 있다고 판단되면 점수로 인정될 수 있다.<br />
2) 파운딩은 2~3회 이상의 데미지를 충분히 줄 수 있는 공격에 대해서만 배점이 이루어진다.<br />
3) KO나 TKO가 아니더라도 다운을 시킬 경우 점수로 인정이 된다.<br />
4) 데미지가 느껴지지 않는 소극적 공격은 그 횟수가 많다 하여도 점수로 인정되지 않는다.<br />
(2) 그라운드 상황에서 포지션 점유 및 기술사용 정도<br />
1) 테이크 다운<br />
① 테이크 다운을 당한 상대방의 등을 완벽히 제압해 바닥에 눌러 놓아 유리한 포지션을 3초 이상 점유하여야 한다.<br />
② 테이크 다운을 시도했으나 상대방이 케이지에 몸이 기대어져 하체(힙)만 다운된 상황에 상대방이 일어나기 위한 적극적인 공방을 펼치지 않거나 주저앉은 그대로 그라운드 상황을 고착시키며 휴식을 취할 경우 테이크 다운 성공으로 판단하여 채점한다.<br />
2) 클린 테이크 다운<br />
① 허리 이상의 높이에서 상대방을 크게 던지기, 메치기 혹은 태클로 크게 넘어뜨렸을 경우 상대방을 제압했는지의 여부를 따지지 않고 클린 테이크 다운으로 점수가 주어진다.<br />
② 클린 테이크 다운과 동시에 유효 포지션 룰과 상관없이 탑 포지션을 3초 이상 점유 하는 경우 포지션 점유에도 점수가 주어진다.<br />
③ 점수로 연결되는 유효 포지션은 마운트 포지션, 사이드 포지션, 백 포지션 이며, 니 온 밸리, 남북, 터틀 등은 포지션 변경 시 움직임의 연결 동선으로 본다.<br />
3) 하위 포지션 선수가 상위 포지션 선수를 뒤집어 유리한 포지션으로 역전해 냈을 때 점수가 주어진다.<br />
(3) 스탠딩 타이밍<br />
1) 양 선수가 그라운드에서 경기를 진행하는 데 있어서 적극성을 보이지 않고 휴식을 취한다든지 데미지가 느껴지지 않는 무의미한 공격을 유지하고 있을 경우 심판 판단에 의해 바로 일으켜 세울 수 있다.<br />
6. 자동 실격패<br />
(1) 선수가 경기 시작 60분 전까지 경기장에 나타나지 않는 경우는 자동적으로 실격패가 된다.<br />
(2) 세컨드가 경기 중 경기를 방해하거나 상대 측 선수와 접촉이 있거나 케이지 위에 올라올 경우, 해당 선수는 자동적으로 실격패로 처리된다.<br />
7. 경기무효<br />
(1) 주심 혹은 대회사의 권한을 대행하는 자가 경기 도중 사고로 경기를 중단시킬 경우, 해당 경기는 무효로 처리될 수 있다.<br />
(2) 대기실에서 심판의 지시를 따르지 않고 난동을 부리거나 비정상적인 행동으로 룰에 위배되는 행위를 범해 경기 진행이 어렵다고 판단될 때 대기실 운영 심판은 심판진과 대회사에게 전달해서 경기몰수 패를 선언할 수 있다.<br />
심판의 권위는 절대적이며 본 대회의 원활한 진행을 위해 선수 및 코치진은 룰에 의거한 심판진의 지시사항 및 전달사항에 대하여 절대적으로 따라 줄 것을 명시하는 바이다.<br />
판정의 대한 불만이나 동의하지 않는 사항, 불합리하다고 생각되는 경기 진행에 대해 재심이나 이의를 제기할 시 서면을 통해 대회사에 제출하여 원만한 해결을 하도록 한다.<br />
* 본 룰은 심판위원회와 대회사의 합의에 따라 개정될 수 있다.<br /><br /><br />


