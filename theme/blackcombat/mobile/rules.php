30. 선수가 자신의 글러브 및 상대방의 글러브에 손가락을 넣어 붙잡는 행위 또는 글러브의 고정 끈에 손가락을 끼워 넣는 행위<br />
31. 선수가 자신의 파이트 쇼츠 및 상대방의 파이트 쇼츠를 붙잡는 행위<br />
