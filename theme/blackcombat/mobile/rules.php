5. 스탠딩과 그라운드에서 후두부, 척추라인을 제외한 부위에 대한 킥, 펀치, 꺾기, 조르기 등 사용 가능<br />
6. 그라운드 시 반칙 부위를 제외한 모든 공격 가능 (안면 공격 허용)<br />
7. 알리&이노끼 상황에서 모든 킥 공격 (스탬핑 킥, 싸커킥, 업 킥 등)을 허용한다.<br /><br /><br />


