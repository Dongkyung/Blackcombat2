<h3>제 3 조 경기장비</h3>
1. 오픈 핑거 글러브<br />
(1) 경기용 대회 공식 글러브는 대회사가 제공 또는 승인하는 오픈 핑거 글러브로 한다.<br />
(2) 선수의 글러브 착용은 반드시 경기 시작 전 지정한 장소에서 착용되어야 한다.<br />
(3) 심판은 대기실에서 선수에게 글러브를 착용 시킨 후, 손목 테이핑 접합부분에 확인 사인을 하여야 한다.<br />
(4) 선수는 해당 경기가 종료되기 전까지 글러브와 테이핑을 해체할 수 없다.<br />
(5) 선수는 경기 시작 전 대기실에서 심판에게 1차로 글러브 검사 및 경기 안전 체크를 받고 케이지에 올라가기 전 2차로 체크를 받는다.
2. 테이핑<br />
(1) 심판진 허가에 의해 무릎(Knee), 정강이(Shin), 팔꿈치(Elbow), 발목(Ankle)등의 테이핑이 가능하다.<br />
(2) 주먹의 밴디지 재료는 대회사가 지정한 물품 또는 지급한 테이프 및 붕대, 허가한 물품 및 테이프에 한정하여 허용된다.<br />
1) 변칙적인 방법을 이용한 주먹고정, 보형물 삽입 등은 철저히 금지된다.<br />
3. 마우스피스<br />
(1) 대회의 경기에서 모든 선수는 마우스피스를 반드시 착용하여야 한다.<br />
(2) 마우스피스를 착용하지 않은 선수는 경기 참가가 불가하며, 경기 실격패의 사유가 된다.<br />
4. 파울 컵<br />
(1) 대회의 경기에서 모든 선수는 파울 컵(낭심 보호대)을 반드시 착용하여야 한다.<br />
(2) 파울 컵을 착용하지 않은 선수는 경기 참가가 불가하며, 경기 실격패의 사유가 된다.<br />
5. 콘택트 렌즈<br />
(1) 하드 콘택트 렌즈는 원칙적으로 사용이 불가하다.<br />
(2) 소프트 콘택트 렌즈에 한하여, 착용이 가능하나, 콘택트 렌즈를 착용하고, 경기하는 과정에서 발생한 선수의 사고, 부상 등 어떤 결과에 대해서도 대회사는 책임을 지지 아니한다.<br />
6. 피부 도포제류 및 호흡기 관련 약제류 등의 사용<br />
(1) 출전 선수에 대한 선수 및 세컨드의 자의적인 바셀린 도포는 일절 금지하며, 선수 및 세컨드는 각종 도포제, 호흡기 관련 약제류 등을 지참하고 경기장에 입장할 수 없다.<br />
(2) 선수의 안면 T-ZONE에 대한 바셀린 도포는 선수의 부상 방지를 목적으로 하며, 등장 시에 심판이 직접 도포한다.<br />
(3) 경기 진행 중의 바셀린 도포는 원칙적으로 금지되나 대회와 경기 진행을 위한 응급처치 용도로 대회 닥터 및 심판이 확인하고 처치한 사항은 예외로 한다.<br />
(4) 닥터 및 심판의 허가 없이 선수의 모든 신체 부위에 대한 도포제, 기관지 확장 약제류, 산소 공급 스프레이 등을 도포, 흡입, 복용하는 행위는 철저히 금지되며, 이를 위반한 경우 반칙이 적용되고 실격패의 사유가 되며, 해당 선수는 징계의 대상이 된다.<br />
(5) 머리카락에 바르는 젤, 왁스, 오일 등을 절대 금지하고 머리카락에는 그 무엇도 바르지 않는다.<br /><br /><br />


<h3>제 4 조 경기 시간</h3>
1. 경기 시간<br />
(1) 모든 경기는 5분 3라운드이다.<br />
(2) 선수 공격에 의해 승부 결정이 나지 않은 경우에 배심의 판정으로 최종 승부를 결정한다.<br />
(3) 경기 전에 대회사가 허가하고 양측 선수가 무승부 상황에 대해 연장전을 합의한 경우, 5분 1라운드 추가 연장을 할 수 있다.<br />
2. 모든 경기의 진행에서 라운드 사이 휴식시간은 1분으로 한다.<br />
3. 모든 경기에서 배심은 3심제로 한다.<br /><br /><br />


<h3>제 5 조 복장/유니폼</h3>
모든 선수의 유니폼은 원칙적으로 대회사가 지정한 유니폼과 경기복만 착용이 가능하며, 이를 위반한 선수 및 팀은 징계의 대상이 된다.<br />
1. 대회사의 경기에 참가하는 모든 선수는 그 유니폼으로 하체 트렁크를 착용해야 한다.<br />
2. 선수와 함께 입장하는 팀의 복장은 통일해야 하며, 세컨드는 선수와 함께 팀 복장을 착용하는 것을 의무로 한다.<br /><br /><br />


<h3>제 6 조 허용 가능 격투 기술</h3>
1. 타격 기술 : 치기, 지르기, 차기, 찍기, 밟기 등<br />
2. 혼합 기술 : 메치기, 던지기, 태클, 다리 걸기 등<br />
3. 유술기 : 관절기, 굳히기, 조르기, 꺾기, 누르기 등<br />
